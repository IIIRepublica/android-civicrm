package org.upsam.civicrm.activity.model;

public class ActivityStatus {
	/**
	 * Valor númerico del estado. Es el valor de status_id en las Activities
	 */
	private int value;
	/**
	 * ID
	 */
	private int id;
	/**
	 * Nombre a mostrar de la actividad
	 */
	private String label;
	/**
	 * Nombre interno de la actividad
	 */
	private String name;

	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}

	/**
	 * @param value
	 *            the value to set
	 */
	public void setValue(int value) {
		this.value = value;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}

	/**
	 * @param label
	 *            the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
